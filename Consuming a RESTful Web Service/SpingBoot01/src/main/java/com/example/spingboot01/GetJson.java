@RestController
@RequestMapping("/get")
public class GetJson {
    @RequestMapping("/json")
    public Quote get(){
        Quote quote = new Quote();
        quote.setType("SUCCESS");
        Value value = new Value();
        value.setId(1L);
        value.setQuote("hello");
        quote.setValue(value);
        return quote;
    }
}
